class TextureManager {
    constructor() {
        this.textures = {};
        this.initializeTextures();
        this.textures.sky = null;
    }

    initializeTextures() {
        this.textures.brick = this.createTextureFromData(BRICK_TEXTURE_DATA, 'brick');
        this.textures.door = this.createTextureFromData(DOOR_TEXTURE_DATA, 'door');
        this.textures.carpet = this.generateCarpetTexture();
        
        // Cache pixel data for performance
        this.textures.brickPixelData = this.textures.brick.getContext('2d').getImageData(0, 0, 32, 32).data;
        this.textures.doorPixelData = this.textures.door.getContext('2d').getImageData(0, 0, 32, 32).data;
        this.textures.carpetPixelData = this.textures.carpet.getContext('2d').getImageData(0, 0, 64, 64).data;
    }

    createTextureFromData(data, baseColor) {
        const texCanvas = document.createElement('canvas');
        texCanvas.width = 32;
        texCanvas.height = 32;
        const texCtx = texCanvas.getContext('2d');
        
        const imageData = texCtx.createImageData(32, 32);
        const pixels = imageData.data;
        
        for (let i = 0; i < data.length; i++) {
            const pixelIndex = i * 4;
            const intensity = data[i] * 255;
            
            if (baseColor === 'brick') {
                pixels[pixelIndex] = intensity * 0.7;
                pixels[pixelIndex + 1] = intensity * 0.3;
                pixels[pixelIndex + 2] = intensity * 0.2;
            } else if (baseColor === 'door') {
                pixels[pixelIndex] = intensity * 0.4;
                pixels[pixelIndex + 1] = intensity * 0.3;
                pixels[pixelIndex + 2] = intensity * 0.1;
            }
            
            pixels[pixelIndex + 3] = 255;
        }
        
        texCtx.putImageData(imageData, 0, 0);
        return texCanvas;
    }

    generateCarpetTexture() {
        const texCanvas = document.createElement('canvas');
        texCanvas.width = 64;
        texCanvas.height = 64;
        const texCtx = texCanvas.getContext('2d');

        texCtx.fillStyle = '#d4a850';
        texCtx.fillRect(0, 0, 64, 64);

        // Add random noise for carpet texture variation
        for (let y = 0; y < 64; y++) {
            for (let x = 0; x < 64; x++) {
                if (Math.random() > 0.7) {
                    const brightness = Math.random() * 40 - 20;
                    const r = Math.max(0, Math.min(255, 212 + brightness));
                    const g = Math.max(0, Math.min(255, 168 + brightness));
                    const b = Math.max(0, Math.min(255, 80 + brightness));
                    texCtx.fillStyle = `rgb(${r},${g},${b})`;
                    texCtx.fillRect(x, y, 1, 1);
                }
            }
        }

        return texCanvas;
    }

    getTexture(name) {
        return this.textures[name];
    }

    getPixelData(name) {
        return this.textures[name + 'PixelData'];
    }

    setSkyTexture(imageElement) {
        this.textures.sky = imageElement;
    }
}